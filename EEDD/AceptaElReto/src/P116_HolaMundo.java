import java.util.Scanner;

public class P116_HolaMundo {
    public static void main(String[] args) {
        Scanner leer = new Scanner(System.in);
       
        int casos;
       
        casos = leer.nextInt();
        for (int i = 0; i < casos; i++) {
            System.out.println("Hola mundo.");
        }
    }
}



  
        
        
       
        

